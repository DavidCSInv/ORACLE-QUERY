SET SERVEROUTPUT ON;

DECLARE
      VFIRST_NAME EMPLOYEES.FIRST_NAME%TYPE;
      VLAST_NAME  EMPLOYEES.LAST_NAME%TYPE;
      VSALARY     EMPLOYEES.SALARY%TYPE;
      VEMPLOYEE_ID EMPLOYEES.EMPLOYEE_ID%TYPE := 128;
BEGIN 
     SELECT FIRST_NAME,LAST_NAME,SALARY
     INTO VFIRST_NAME,VLAST_NAME,VSALARY
     FROM EMPLOYEES
     WHERE EMPLOYEE_ID = VEMPLOYEE_ID;
     DBMS_OUTPUT.PUT_LINE ('Employee_id = ' || VEMPLOYEE_ID);
     DBMS_OUTPUT.PUT_LINE ('First_name = ' || VLAST_NAME);
     DBMS_OUTPUT.PUT_LINE ('LAST_NAME = ' || VLAST_NAME);
     DBMS_OUTPUT.PUT_LINE ('Salary = ' || VSALARY);  
END;
