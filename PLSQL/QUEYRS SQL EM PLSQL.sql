========= UPDATES EM PL/SQL ======
    
    DECLARE 
            VEMPLOYEE_ID    EMPLOYEES.EMPLOYEE_ID%TYPE;
            VPERCENTUAL     NUMBER (3) := 10;
        BEGIN 
         UPDATE EMPLOYEES
         SET SALARY = SALARY * (1 + VPERCENTUAL / 100)
         WHERE EMPLOYEE_ID = VEMPLOYEE_ID;
        COMMIT;
    END;
    
    
